package org.example.figuras;

/**
 * Clase abstracta que representa una figura geométrica.
 * Define el metodo abstracto calcularArea() que debe ser implementado por cada figura.
 */
public abstract class Figura {
    /**
     * Metodo abstracto para calcular el área de la figura.
     * @return Área como valor double.
     */
    public abstract double calcularArea();
}
